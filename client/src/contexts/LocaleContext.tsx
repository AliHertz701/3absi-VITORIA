// Arabic pluralization rules
const arPluralRules = (count: number): string => {
  if (count === 1) return '';
  if (count === 2) return 'ان';
  if (count > 2 && count < 11) return 'ات';
  return '';
};

const enPluralRules = (count: number): string => {
  return count === 1 ? '' : 's';
};

export function LocaleProvider({ children }: { children: React.ReactNode }) {
  const [locale, setLocale] = useState<Locale>(() => {
    const saved = localStorage.getItem('vitoria_locale');
    return (saved as Locale) || 'en';
  });

  const isRTL = locale === 'ar';

  useEffect(() => {
    localStorage.setItem('vitoria_locale', locale);
    document.documentElement.dir = isRTL ? 'rtl' : 'ltr';
    document.documentElement.lang = locale;
  }, [locale, isRTL]);

  const t = (key: string, params?: Record<string, string>): string => {
    const translations = locale === 'ar' ? arTranslations : enTranslations;
    const raw = translations[key];
    let translation: string;
    if (Array.isArray(raw)) {
      translation = raw.join('\n\n');
    } else {
      translation = (raw as string) || key;
    }
    
    // Handle pluralization
    if (params?.count) {
      const count = parseInt(params.count);
      const pluralSuffix = locale === 'ar' ? arPluralRules(count) : enPluralRules(count);
      translation = translation.replace('{plural}', pluralSuffix);
    }
    
    // Replace other parameters
    if (params) {
      Object.keys(params).forEach(param => {
        translation = translation.replace(`{${param}}`, params[param]);
      });
    }
    
    return translation;
  };

  const handleSetLocale = (newLocale: Locale) => {
    setLocale(newLocale);
  };

  return (
    <LocaleContext.Provider value={{ locale, setLocale: handleSetLocale, t, isRTL }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    throw new Error('useLocale must be used within a LocaleProvider');
  }
  return context;
}